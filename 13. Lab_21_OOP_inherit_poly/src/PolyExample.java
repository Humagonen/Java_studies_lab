
public class PolyExample {
	
	public void multi2int(int a, int b) {
		System.out.println(a + " x " + b + " = " + a*b);
	}
	
	public void multi3int(int a, int b, int c) {
		System.out.println(a + " x " + b + " x " + c + " = " + a*b*c);
	}
	
	public void multi2double(double a, double b) {
		System.out.println(a + " x " + b + " = " + a*b);
	}
	
	public void multi3double(double a, double b, double c) {
		System.out.println(a + " x " + b + " x " + c + " = " + a*b*c);
	}

}
