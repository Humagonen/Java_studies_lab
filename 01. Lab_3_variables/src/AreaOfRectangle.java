public class AreaOfRectangle {
	
    public static void main(String[] args) {
        // Declare and assign variables for length and width
        double l = 2.5;
        int w = 5;
        
        // Expression to calculate the area
        double A = l * w;
        
        // Print the result
        System.out.println("Area of the rectangle: " + A);
    }
}